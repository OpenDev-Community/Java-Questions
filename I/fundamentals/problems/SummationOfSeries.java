public class SummationOfSeries {

    public static void main(String[] args) {
        System.out.println("Summation of a Series");

        // summation variable
        double summation = 1 + 2 + 3 + 4 + 5 + 6 + 7 + 8 + 9;

        // ouput
        System.out.println("Summation of number from 1 to 9 is: " + summation);
    }
}