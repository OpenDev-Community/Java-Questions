public class DisplayPattern {
    public static void main(String[] args) {
        // JAVA pattern
        // Empty line
        System.out.println();
        System.out.println("    J     A   V     V   A");
        System.out.println("    J    A A   V   V   A A");
        System.out.println("    J   AAAAA   V V   AAAAA");
        System.out.println("J J J  A     A   V   A     A");
                
    }
}
